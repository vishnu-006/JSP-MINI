package JavaBean;

public class Book {
	private int bid;
	private String bname;
	private double bprice;
	public int getBid() {
		return bid;
	}
	public void setbid(int bid) {
		this.bid = bid;
	}
	public String getbname() {
		return bname;
	}
	public void setbname(String bname) {
		this.bname = bname;
	}
	public double getbprice() {
		return bprice;
	}
	public void setbprice(double bprice) {
		this.bprice = bprice;
	}
	

}
